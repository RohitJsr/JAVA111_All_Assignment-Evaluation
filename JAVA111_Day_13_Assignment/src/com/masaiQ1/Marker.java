package com.masaiQ1;

public interface Marker {
	/*
	 A marker interface is an interface that has no methods
	  or constants inside it. It provides run-time type information 
	  about objects, so the compiler and JVM have additional information about
	   the object.
	A marker interface is also called a tagging interface.
	 The Serializable and Cloneable interfaces are the example of marker interface. In short,
	  it indicates a signal or command to the JVM.
	  The declaration of marker interface is the same as interface in Java but the interface must be empty. For example:

	public interface Serializable   
	{  
	  
	}  
	 */

}
